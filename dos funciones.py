def cuadrado():
    valor = int(input("Ingrese un entero > "))
    valor2 = valor**2
    return valor2

def producto():
    valor = int(input("Ingrese un entero > "))
    valor2 = int(input("Ingrese otro entero > "))
    valor3 = valor*valor2
    return valor3


